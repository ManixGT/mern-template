import { Schema } from "mongoose";

const userSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    roles: [{ type: String, enum: ["admin", "user"] }],
}, {
    timestamps: true
})

export default userSchema;